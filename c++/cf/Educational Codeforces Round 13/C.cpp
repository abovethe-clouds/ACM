int read()
{
    int x = 0, w = 1;
    char ch = 0;
    while (ch < '0' || ch > '9')
    {
        if (ch == '-') w = -1;
        ch = getchar();
    }
    while (ch >= '0' && ch <= '9')
    {
        x = x * 10 + (ch - '0');
        ch = getchar();
    }
    return x * w;
}

void solve()
{
    ll n,a,b,p,q;
    cin>>n>>a>>b>>p>>q;
    ll lcm=a/__gcd(a,b)*b;

    ll ans3=n/lcm;
    ll ans1=n/a-ans3;
    ll ans2=n/b-ans3;
    cout<<ans1*p+ans2*q+ans3*max(q,p)<<endl;
}

